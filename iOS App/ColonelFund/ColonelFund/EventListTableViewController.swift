class EventListTableViewController: UITableViewController {
    
    //MARK: Properties
    let ec = EventCollection()
    var eventList: [Event] = []
    
    
    override func viewDidLoad() {
        super.viewDidLoad()
        eventList = ec.getEvents()
        
